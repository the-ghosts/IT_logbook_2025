#Working with a list
fruits = ["lemon", "carrot", "cucumber", "berry"]
print(f"List of fruit available: {fruits}")

fruits.append("orange")
print(f"Adding more fruit: {fruits}")

fruits.remove("lemon")
print(f"Remaining fruits after buying last lemon: {fruits}")

print(f"First fruit: {fruits[0]}")
print(f"Number of fruits available: {len(fruits)}")

#Working with tuple
foods = ("Rice", "Yam", "Beans", "Cassava Flake")
print(f"List of foods available: {foods}")

print(f"Second food: {foods[1]}")
